package program1_take2;

public class NodeV 
{
///////////////////////
	private int id;
	private int weight;
///////////////////////
	public NodeV()
	{
		
	}
///////////////////////
	public NodeV(int id_temp, int weight_temp)
	{
		id = id_temp;
		weight = weight_temp;
	}
///////////////////////
	public int weight()
	{
		return weight;
	}
///////////////////////
	public int id()
	{
		return id;
	}
///////////////////////
}
